void app_general_task(void)
{
    static sdt_bool sys_cfged = sdt_false;

    if(sys_cfged)
    {
        mde_ledBlink_task();
        app_grating_task();
    }
    else
    {
        sys_cfged = sdt_true;
        mde_systemClock_cfg();
    }
}
