//++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
#define used_solid  0
//++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
void app_electrimeter_task(void)
{
    mde_mRtu_master_task();

    macro_createTimer(timer_transmit,timerType_millisecond,0);
    pbc_timerClockRun_task(&timer_transmit);
    if(pbc_pull_timerIsCompleted(&timer_transmit))
    {
        pbc_reload_timerClock(&timer_transmit,500);
        mde_mRtu_master_cmd0x03_transmit(used_solid,0x01,0x1000,0x0003);

    }

}
